#!/usr/bin/env node
import * as fs from "node:fs";
import * as path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, "..");
const SOURCE_DIR = path.join(ROOT, "packages/ai/.config/kilo/agents");
const OPENCODE_DIR = path.join(ROOT, "packages/ai/.config/opencode/agents");
const CODEX_DIR = path.join(ROOT, "packages/ai/.codex/agents");
const CODEX_SKILLS_DIR = path.join(ROOT, "packages/ai/.codex/skills");
const CLAUDE_CODE_DIR = path.join(ROOT, "packages/ai/.claude/agents");

function parseYaml(yaml) {
  const lines = yaml.split("\n").filter(l => {
    const t = l.trim();
    return t !== "" && !t.startsWith("#");
  });
  const root = {};
  const stack = [{ indent: -1, obj: root }];

  for (const line of lines) {
    const indent = line.length - line.trimStart().length;
    const trimmed = line.trim();

    while (stack.length > 1 && indent <= stack[stack.length - 1].indent) {
      stack.pop();
    }

    const colonIdx = trimmed.indexOf(":");
    if (colonIdx === -1) continue;

    let key = trimmed.slice(0, colonIdx).trim();
    const rest = trimmed.slice(colonIdx + 1).trim();

    if (key.startsWith('"') && key.endsWith('"')) {
      key = key.slice(1, -1);
    }

    const parent = stack[stack.length - 1].obj;

    if (rest === "") {
      const child = {};
      parent[key] = child;
      stack.push({ indent, obj: child });
    } else {
      parent[key] = rest.startsWith('"') && rest.endsWith('"') ? rest.slice(1, -1) : rest;
    }
  }

  return root;
}

function readAgent(agentPath) {
  const text = fs.readFileSync(agentPath, "utf-8");
  const match = text.match(/^---\n(?<frontmatter>[\s\S]*?)\n---\n(?<prompt>[\s\S]*)$/);
  if (!match) throw new Error(`missing YAML frontmatter: ${agentPath}`);

  const frontmatter = parseYaml(match.groups.frontmatter);
  if (!frontmatter.description) {
    throw new Error(`missing description in agent: ${agentPath}`);
  }

  return {
    name: path.basename(agentPath, ".md"),
    path: agentPath,
    frontmatter,
    prompt: match.groups.prompt,
  };
}

function codexModel(model) {
  if (!model) return null;
  return model.startsWith("openai/") ? model.slice("openai/".length) : model;
}

function codexSandbox(frontmatter) {
  const edit = frontmatter.permission?.edit;
  return edit === "deny" ? "read-only" : null;
}

function claudeModel(model) {
  if (!model) return "opus";
  if (/gpt-4|gpt-5/.test(model)) return "opus";
  if (/gpt-3\.5/.test(model)) return "haiku";
  return "sonnet";
}

function claudeTools(frontmatter) {
  const permission = frontmatter.permission || {};
  const tools = [];
  if (permission.read?.["*"] !== "deny") tools.push("Read");
  if (permission.edit?.["*"] !== "deny") tools.push("Edit");
  if (permission.bash !== "deny") tools.push("Bash");
  if (permission.webfetch !== "deny") tools.push("WebFetch");
  return tools.length > 0 ? tools : null;
}

function dumpYaml(obj, indent = 0) {
  const prefix = " ".repeat(indent);
  const lines = [];

  for (const [key, value] of Object.entries(obj)) {
    if (value === null || value === undefined) continue;
    if (Array.isArray(value)) {
      lines.push(`${prefix}${key}:`);
      for (const item of value) {
        lines.push(`${prefix}  - ${JSON.stringify(item)}`);
      }
    } else if (typeof value === "object") {
      lines.push(`${prefix}${key}:`);
      lines.push(dumpYaml(value, indent + 2));
    } else {
      lines.push(`${prefix}${key}: ${JSON.stringify(value)}`);
    }
  }

  return lines.join("\n");
}

function claudeCodeMarkdown(agent) {
  const description = agent.frontmatter.description;
  const model = claudeModel(agent.frontmatter.model);
  const tools = claudeTools(agent.frontmatter);

  const frontmatter = { name: agent.name, description, model };
  if (tools) frontmatter.tools = tools;

  return `---\n${dumpYaml(frontmatter)}\n---\n${agent.prompt}`;
}

function tomlMultilineLiteral(value) {
  if (value.includes("'''")) {
    throw new Error("prompt contains unsupported TOML literal delimiter");
  }
  return "'''\n" + value.replace(/\n$/, "") + "\n'''";
}

function relPath(absPath) {
  return absPath.startsWith(ROOT + "/") ? absPath.slice(ROOT.length + 1) : absPath;
}

function codexToml(agent) {
  const description = agent.frontmatter.description;
  const model = codexModel(agent.frontmatter.model);
  const sandbox = codexSandbox(agent.frontmatter);

  const lines = [
    `# Generated by scripts/generate-ai-agents.mjs from ${relPath(agent.path)}.`,
    "# Do not edit directly; update the source Markdown agent instead.",
    `name = ${JSON.stringify(agent.name)}`,
    `description = ${JSON.stringify(description)}`,
  ];
  if (model) lines.push(`model = ${JSON.stringify(model)}`);
  if (sandbox) lines.push(`sandbox_mode = ${JSON.stringify(sandbox)}`);
  lines.push(`developer_instructions = ${tomlMultilineLiteral(agent.prompt)}`);
  return lines.join("\n") + "\n";
}

function codexSkill(agent) {
  const description = agent.frontmatter.description;

  return [
    "---",
    `# Generated by scripts/generate-ai-agents.mjs from ${relPath(agent.path)}.`,
    "# Do not edit directly; update the source Markdown agent instead.",
    `name: ${agent.name}`,
    `description: ${JSON.stringify(description)}`,
    "---",
    `# ${agent.name}`,
    agent.prompt.trim(),
    "",
  ].join("\n");
}

function generate(agents, check) {
  const outputs = {};

  for (const agent of agents) {
    outputs[path.join(OPENCODE_DIR, `${agent.name}.md`)] = fs.readFileSync(agent.path, "utf-8");
    outputs[path.join(CODEX_DIR, `${agent.name}.toml`)] = codexToml(agent);
    outputs[path.join(CLAUDE_CODE_DIR, `${agent.name}.md`)] = claudeCodeMarkdown(agent);
  }

  const overseer = agents.find(a => a.name === "overseer");
  if (!overseer) throw new Error("missing overseer agent");

  outputs[path.join(CODEX_SKILLS_DIR, "overseer", "SKILL.md")] = codexSkill(overseer);

  const changed = Object.entries(outputs).filter(([p, c]) => {
    let existing;
    try { existing = fs.readFileSync(p, "utf-8"); } catch { return true; }
    return existing !== c;
  });

  if (check) {
    if (changed.length === 0) {
      console.log("Generated OpenCode, Codex, and Claude Code agents are up to date.");
      return true;
    }
    const changedPaths = changed.map(([p]) => relPath(p)).sort();
    console.error("Generated agents are stale:");
    for (const p of changedPaths) console.error(`  ${p}`);
    return false;
  }

  fs.mkdirSync(OPENCODE_DIR, { recursive: true });
  fs.mkdirSync(CODEX_DIR, { recursive: true });
  fs.mkdirSync(CLAUDE_CODE_DIR, { recursive: true });
  fs.mkdirSync(path.join(CODEX_SKILLS_DIR, "overseer"), { recursive: true });

  for (const [filePath, content] of Object.entries(outputs)) {
    fs.writeFileSync(filePath, content, "utf-8");
  }

  const rootRel = ROOT + "/";
  console.log(`Generated ${agents.length} OpenCode agents in ${OPENCODE_DIR.replace(rootRel, "")}`);
  console.log(`Generated ${agents.length} Codex agents in ${CODEX_DIR.replace(rootRel, "")}`);
  console.log(`Generated ${agents.length} Claude Code agents in ${CLAUDE_CODE_DIR.replace(rootRel, "")}`);
  console.log(`Generated Codex Overseer skill in ${CODEX_SKILLS_DIR.replace(rootRel, "")}/overseer`);
  return true;
}

const args = process.argv.slice(2);
const checkIndex = args.indexOf("--check");
let check = false;
if (checkIndex !== -1) {
  check = true;
  args.splice(checkIndex, 1);
}
if (args.length > 0) {
  throw new Error(`unknown arguments: ${args.join(" ")}`);
}

const agentPaths = fs.readdirSync(SOURCE_DIR)
  .filter(f => f.endsWith(".md"))
  .sort()
  .map(f => path.join(SOURCE_DIR, f));

const agents = agentPaths.map(readAgent);
process.exit(generate(agents, check) ? 0 : 1);
